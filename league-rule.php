1. プライバシーポリシー（個人情報保護方針）
このポリシーは、運営がユーザーの情報をどう扱うかを説明するものです。

プライバシーポリシー
B2L運営事務局（以下「当事務局」）は、本サービス（ウェブサイトおよびLINE連携機能）における利用者の個人情報の取扱いについて、以下の通りプライバシーポリシーを定めます。

1. 取得する情報
当事務局は、本サービスの提供にあたり、以下の情報を取得します。

LINEアカウントの内部識別子（ユーザーID）

LINEのプロフィール情報（表示名、プロフィール画像）

リーグ運営に必要な情報（氏名、チーム名、連絡先等）

2. 利用目的
取得した情報は、以下の目的で利用します。

リーグ運営、試合管理、および本人確認のため

LINEを通じた事務連絡や通知の送付

お問い合わせへの対応

3. 第三者提供の禁止
法令に基づく場合を除き、利用者の同意なく個人情報を第三者に提供することはありません。

4. 安全管理
当事務局は、個人情報の漏洩、滅失の防止に向けた適切な安全管理措置を講じます。

2. 利用規約
この規約は、ユーザーがリーグに参加する上でのルールを定めたものです。

B2L利用規約

第1条（目的）
本規約は、B2L運営事務局（以下「当事務局」）が提供するリーグ管理システムおよびLINE連携サービス（以下「本サービス」）の利用条件を定めるものです。

第2条（LINE連携）

利用者は、自身のLINEアカウントを本サービスと連携させることにより、通知の受信やマイページ機能を利用できるものとします。

LINEアカウントの管理責任は利用者が負うものとし、不正利用等により生じた損害について当事務局は責任を負いません。

第3条（禁止事項）
利用者は以下の行為を禁止されます。

虚偽の情報登録

他の利用者や当事務局への迷惑行為・誹謗中傷

システムの不正操作やリバースエンジニアリング

第4条（サービスの停止・変更）
当事務局は、メンテナンスや運営上の都合により、事前の通知なくサービスの内容を変更または一時停止することがあります。

第5条（免責事項）
本サービスの利用により発生した利用者間のトラブルや損害について、当事務局は一切の責任を負わないものとします。